//used to store player's name and score

public class Player {
    private String name;
    private int score;

    public void setName(String name){

        this.name = name;
    }
    public String getName() {

        return this.name;
    }

    public void setScore(int score){
        this.score = score;

    }

    public int getScore() {
        return this.score;

    }


}
